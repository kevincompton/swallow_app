            <h2>What is an edible?</h2>
            <p>Edibles are food infused with cannabis or cannabis oils. Edibles come in a wide variety of forms including drinks, brownies, cookies, candy and more.</p>
            <img src="/images/edible-education/types.png" />
            
            <h2>Why Edibles?</h2>
            <p>Edibles are discrete, delicious, reasonably priced, and effective. Edibles are perfect for those moments when you want to be medicated but can't smoke. Edibles have advanced over the years, which has led to most edibles being lab tested for THC content. By consuming a lab tested edible, you will have more control over the high you wish to receive. If you have had a bad high and are afraid of edibles, keep reading to learn how to enjoy edibles the right way.</p>

            <h2>Sublingual vs. Ingestible Consumption</h2>
            <p>Liquid edibles, like drinks or added drops like tinctures, kick in quicker than solid foods. Different edibles are absorbed into the body at different rates.</p>

            <h5>Sublingual Definition:</h5>
            <p>Sublingual is anything ingested by applying under the tongue or through the membranes of the mouth. These could include things dissolved in alcohol bases such as tinctures, but also things like mints, lozenges, and mouth sprays.
            When an edible is absorbed through the saliva and oral membranes, it can reach your brain within minutes. When you consume food, it has to pass through your stomach, digestive track and liver, which can take 30 minutes to an hour to begin to feel the effects.
            Mints and tinctures are absorbed through the mouth and get into the body much quicker than edibles you fully digest. After orally consuming an edible, your body needs to digest as metabolize the food before you feel the effects.</p>




            <h3>READ THE PACKAGING</h3>
                <p>Edibles affect everyone differently based on personal tolerance and individual cannabinoid system. Some edibles include a recommended dose, which may not apply to everyone as this does not take body type or experience into account. Try to stick with edibles that have been lab tested for consistency and accuracy of dosages.</p>

                <p>Edibles can take 30 minutes up to 2 hours to kick in, so much sure to watch the clock before consuming more. Discovering your tolerance will take a lot of trial and error. Be patient and most importantly, be safe.</p>

            <h3>NEW USERS</h3>
                <p>Determine your tolerance by starting with smaller doses at 5 mg or 10 mg THC.</p>

            <h3>EXPERIENCED USERS</h3>
                <p>Edibles may still affect you strongly. Start small and work your way up.</p>

                <p>Calculate the amount of THC in each portion you consume. If you have a bag of 100 mg candy that has 10 pieces, each piece of can't is roughly 10 mg THC. Edibles' effects can be stronger than smoking cannabis so use with caution.</p>

                <p>Never underestimate the power of a small bite. Small edibles can be very strong too! It's also important to remember that edibles are not always consistent batch to batch.</p>

            <h3>Adverse Effects</h3>
            <p>Consuming too much THC can result in a bad experience that may be uncomfortable. You may experience hallucinations, anxiety, paranoia, and/or drowsiness.</p>
